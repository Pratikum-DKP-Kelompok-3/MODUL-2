angka = float(input("Masukan angka yang Anda inginkan: "))
if angka % 2==0:
    print("Angka yang Anda masukkan merupakan angka genap" %angka)
elif angka % 2==1:
    print("Angka yang Anda masukkan merupakan angka ganjil %s" %angka)
else:
    print("Angka yang Anda masukkan bukan bilangan bilat bulat yaitu %s" %angka)
   